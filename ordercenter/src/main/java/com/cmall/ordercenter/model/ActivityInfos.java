public class ActivityInfos {
	private String days;
	private String discount;
	private String jinEQuan;
	private String zheKouQuan;
	
	public String getJinEQuan() {
		return jinEQuan;
	}
	public void setJinEQuan(String jinEQuan) {
		this.jinEQuan = jinEQuan;
	}
	public String getZheKouQuan() {
		return zheKouQuan;
	}
	public void setZheKouQuan(String zheKouQuan) {
		this.zheKouQuan = zheKouQuan;
	}
	public String getDays() {
		return days;
	}
	public void setDays(String days) {
		this.days = days;
	}
	public String getDiscount() {
		return discount;
	}
	public void setDiscount(String discount) {
		this.discount = discount;
	}
	
}
