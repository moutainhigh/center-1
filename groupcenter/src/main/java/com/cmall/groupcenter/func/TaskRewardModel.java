package com.cmall.groupcenter.func;


/**
 * 字符串转对象的类
 * @author dyc
 * */
public class TaskRewardModel {
	
	private String name = "";
	private String value = "";
	private String tip = "";
	private String code = "";
	
	/**
	 * 获取  code
	 */
	public String getCode() {
		return code;
	}
	/**
	 * 设置 
	 * @param code 
	 */
	public void setCode(String code) {
		this.code = code;
	}
	/**
	 * 获取  name
	 */
	public String getName() {
		return name;
	}
	/**
	 * 设置 
	 * @param name 
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * 获取  value
	 */
	public String getValue() {
		return value;
	}
	/**
	 * 设置 
	 * @param value 
	 */
	public void setValue(String value) {
		this.value = value;
	}
	/**
	 * 获取  tip
	 */
	public String getTip() {
		return tip;
	}
	/**
	 * 设置 
	 * @param tip 
	 */
	public void setTip(String tip) {
		this.tip = tip;
	}

	
}
